package operators;
import java.util.*;

public class ComparisionOp {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		for(int i = 0 ; i<=100 ; i++) {
	
		
		int a = sc.nextInt();
		int b = sc.nextInt();
//		return boolean
		System.out.println(a == b);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!= b);

	}}

}
