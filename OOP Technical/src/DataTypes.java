public class DataTypes {
	public static void main(String args[]) {
		byte a = 10;
		System.out.println(a);
		short b = 10*128;
		System.out.println(b);
		int c = 1676665;
		System.out.println(c);
		long d = 10024*79556;
		System.out.println(d);
		double e = 99.55;
		System.out.println(e);
		char f = 'G';
		System.out.println(f);
		boolean g = true;
		System.out.println(g);
	}
}
